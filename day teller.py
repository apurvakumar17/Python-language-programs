x=int(input("Enter the day number: "))
if x>=8:
    print("Invalid input")
elif x==1:
    print("Day will be monday")
elif x==2:
    print("Day will be tuesday")
elif x==3:
    print("Day will be wednesday")
elif x==4:
    print("Day will be thursday")
elif x==5:
    print("Day will be friday")
elif x==6:
    print("Day will be saturday")
else:
    print("Day will be sunday")
