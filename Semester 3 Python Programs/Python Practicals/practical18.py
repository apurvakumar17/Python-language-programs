text = input("Enter a string: ")

print("\nString Slicing Results:")
print("Original String:", text)

print("1. First 5 characters:", text[:5])
print("2. Last 5 characters:", text[-5:])
print("3. Characters from index 2 to 7:", text[2:8])
print("4. Every 2nd character:", text[::2])
print("5. String in reverse:", text[::-1])
print("6. From index 3 to end:", text[3:])
print("7. From beginning to index 4:", text[:5])

