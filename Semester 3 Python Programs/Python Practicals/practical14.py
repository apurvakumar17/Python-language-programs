a = 10
b = 5

print("Arithmetic Operators:")
print("a + b =", a + b)
print("a - b =", a - b)
print("a * b =", a * b)
print("a / b =", a / b)
print("a % b =", a % b)
print("a ** b =", a ** b)
print("a // b =", a // b,"\n")

print("Relational Operators:")
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
print("a >= b:", a >= b)
print("a <= b:", a <= b,"\n")

print("Logical Operators:")
print("a > 5 and b < 10:", a > 5 and b < 10)
print("a > 5 or b > 10:", a > 5 or b > 10)
print("not(a > b):", not(a > b),"\n")

print("Assignment Operators:")
x = 10
print("x =", x)
x += 5
print("x += 5 →", x)
x -= 3
print("x -= 3 →", x)
x *= 2
print("x *= 2 →", x)
x /= 4
print("x /= 4 →", x)
x %= 3
print("x %= 3 →", x,"\n")

print("Bitwise Operators:")
print("a & b =", a & b)   # AND
print("a | b =", a | b)   # OR
print("a ^ b =", a ^ b)   # XOR
print("~a =", ~a)         # NOT
print("a << 1 =", a << 1) # Left Shift
print("a >> 1 =", a >> 1,"\n") # Right Shift

print("Membership Operators:")
my_list = [1, 2, 3, 4, 5]
print("3 in my_list:", 3 in my_list)
print("10 not in my_list:", 10 not in my_list,"\n")

print("Identity Operators:")
x = [1, 2, 3]
y = x
z = [1, 2, 3]
print("x is y:", x is y)
print("x is z:", x is z)
print("x is not z:", x is not z)

